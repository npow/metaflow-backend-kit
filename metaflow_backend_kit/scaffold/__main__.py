    def execute(self) -> int:
        """Submit, stream, wait, and return the exit code.

        Infrastructure errors (e.g. quota exceeded, missing image) are caught
        here and cause sys.exit(METAFLOW_EXIT_DISALLOW_RETRY) so that
        Metaflow does not retry a step that can never succeed on this infra.
        NotImplementedError is re-raised immediately so developer-facing stub
        errors are not mistaken for infrastructure failures.
        """
        try:
            self._submit_job()
        except NotImplementedError:
            raise
        except Exception as e:
            # Infrastructure error at submission time — disallow retry.
            # (A job that can\'t be submitted will never succeed on retry either.)
            self._echo(
                f"[{name}] Infrastructure error during job submission: {{e}}",
                stream="stderr",
                bad=True,
            )
            sys.exit(METAFLOW_EXIT_DISALLOW_RETRY)

        try:
            self._stream_logs()
            exit_code = self._wait()
        finally:
            # Always attempt to clean up the remote job on any exit path
            # (normal return, exception, or sys.exit via OOM/timeout branch).
            # _kill() is best-effort and must not raise.
            self._kill()

        # OOM (137) and timeout (124) are infrastructure problems; the user\'s
        # code did not produce a meaningful exit code.
        if exit_code in (137, 124):
            reason = "OOM kill" if exit_code == 137 else "timeout"
            self._echo(
                f"[{name}] Job exited with code {{exit_code}} ({{reason}}).  "
                "Disallowing retry (infra failure).",
                stream="stderr",
                bad=True,
            )
            sys.exit(METAFLOW_EXIT_DISALLOW_RETRY)

        return exit_code

