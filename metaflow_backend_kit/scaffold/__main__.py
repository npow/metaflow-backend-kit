def _default_target_platform() -> str:
    """Detect the remote target platform for conda environment resolution.

    Returns "linux-aarch64" on ARM hosts (Apple Silicon, AWS Graviton),
    "linux-64" everywhere else.  This is the platform the remote container
    runs on — NOT the submitter OS.  Override with
    METAFLOW_{NAME_UPPER}_TARGET_PLATFORM if your container architecture
    differs from the submitter.
    """
    machine = platform.machine().lower()
    if machine in ("aarch64", "arm64"):
        return "linux-aarch64"
    return "linux-64"


def _get_run_time_limit_for_task(decorators: Any, timeout_attr: Any) -> Optional[int]:
    """Extract the effective timeout (seconds) for this task.

    Checks @timeout decorator first, then falls back to the decorator\'s own
    timeout attribute.  Returns None if no timeout is set.
    """
    # Check for @timeout decorator on the step
    for deco in decorators:
        if deco.name == "timeout":
            seconds = deco.attributes.get("seconds") or 0
            minutes = deco.attributes.get("minutes") or 0
            hours = deco.attributes.get("hours") or 0
            total = int(seconds) + int(minutes) * 60 + int(hours) * 3600
            if total > 0:
                return total
    # Fall back to our own timeout attribute
    if timeout_attr:
        return int(timeout_attr)
    return None


