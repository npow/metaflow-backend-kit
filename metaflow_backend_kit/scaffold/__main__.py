def main() -> None:
    if len(sys.argv) < 2:
        print(__doc__)
        print("Usage: python -m metaflow_backend_kit.scaffold <name> [output_dir]")
        sys.exit(1)

    name = sys.argv[1].replace("-", "_")

    if not name.isidentifier():
        print(f"Error: {name!r} is not a valid Python identifier.", file=sys.stderr)
        print("Use only letters, digits, and underscores.", file=sys.stderr)
        sys.exit(1)

    output_dir = sys.argv[2] if len(sys.argv) > 2 else "."
    scaffold(name, output_dir)


if __name__ == "__main__":
    main()
